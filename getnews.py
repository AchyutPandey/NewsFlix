@st.cache_resource
def load_model_and_tokenizer():
    with st.spinner("Loading summarization model... please wait."):
        tokenizer = BartTokenizer.from_pretrained(model_name, cache_dir="models")
        model = BartForConditionalGeneration.from_pretrained(model_name, cache_dir="models")
    return tokenizer, model

bart_tokenizer, bart_model = load_model_and_tokenizer()

def summarize(text, maxSummarylength=500):
    inputs = bart_tokenizer.encode("summarize: " + text,
                                   return_tensors="pt",
                                   max_length=1024, truncation=True)
    summary_ids = bart_model.generate(
        inputs,
        max_length=int(maxSummarylength),
        min_length=int(maxSummarylength / 5),
        length_penalty=10.0,
        num_beams=4,
        early_stopping=True
    )
    summary = bart_tokenizer.decode(summary_ids[0], skip_special_tokens=True)
    return summary

