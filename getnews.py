def process():
  data = fetch_news()
  for i in range (3):
    single_news = data["data"][i]
    news_title=single_news["title"]
    news_text = single_news["snippet"]
    news_url = single_news["url"]
    news_img_url = single_news["image_url"]
    news_source = single_news["source"]
    news_summary = summarize(news_text)
    display(news_title, news_url, news_img_url, news_source, news_summary)

def strmlt():
    st.title("Newsflix")
    if st.button("Load Latest News"):
        with st.spinner("Fetching and summarizing latest news..."):
            process()
